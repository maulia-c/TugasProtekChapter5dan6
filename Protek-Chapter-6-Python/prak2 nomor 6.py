def luasSegitiga(alas,tinggi):
    luas= alas * tinggi / 2
    return luas

alas1=10
tinggi1=20
print("Luas segitiga dengan alas",alas1 ,
      "dan tinggi",tinggi1,
      "adalah",luasSegitiga(alas1,tinggi1))

alas2=15
tinggi2=45
print("Luas segitiga dengan alas",alas2 ,
      "dan tinggi",tinggi2 ,
      "adalah",luasSegitiga(alas2,tinggi2))
